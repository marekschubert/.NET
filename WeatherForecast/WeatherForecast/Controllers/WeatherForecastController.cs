        [HttpGet]
        public IEnumerable<WeatherForecast> Get(int count, int min, int max)
        {
            var rng = new Random();
            return Enumerable.Range(1, count).Select(index => new WeatherForecast
            {
                Date = DateTime.Now.AddDays(index),
                TemperatureC = rng.Next(min, max),
                Summary = Summaries[rng.Next(Summaries.Length)]
            })
            .ToArray();
        }

        [HttpPost("generate")]

        public ActionResult<IEnumerable<WeatherForecast>> Post([FromQuery]int count, [FromBody]TemperatureRequest range)
        {
            if (count <= 0 || range.Min > range.Max)
                return BadRequest("Błędne wartości zapytania");

            var result = Get(count, range.Min, range.Max);
            return Ok(result);
            
        }
